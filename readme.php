--twitter_challenge

This is a twitter api client that displays tweets with a given hashtag and that has been retweeted atleast once.

--Steps to use this api client:

1. Create a new twitter app with access to Read only permissions at https://apps.twitter.com.

2. Set consumer key and consumer secret of this twitter app in main.php file.

3. Set oauth callback url in main.php file and make sure it is same as Callback URL specified at the time of configuration of this twitter app.

4. Open https://kayako-challenge.000webhostapp.com page in browser.